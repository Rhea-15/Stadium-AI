const translations: Record<LanguageName, Record<Key, string>> = { English: en, Hindi: hi, Spanish: es, French: fr, Portuguese: pt, German: de, Arabic: ar };

type I18nContextType = {
  language: LanguageName;
  setLanguage: (l: LanguageName) => void;
  t: (key: Key, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageName>("English");

  useEffect(() => {
    const saved = getSession().language as LanguageName;
    if (saved && translations[saved]) {
      setLanguageState(saved);
      applyDocumentDirection(saved);
    } else {
      applyDocumentDirection("English");
    }
  }, []);

  function setLanguage(l: LanguageName) {
    setLanguageState(l);
    setSession({ language: l });
    applyDocumentDirection(l);
  }

  function t(key: Key, vars?: Record<string, string | number>) {
    let str = translations[language]?.[key] ?? en[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) str = str.replace(`{${k}}`, String(v));
    }
    return str;
  }

  return <I18nContext.Provider value={{ language, setLanguage, t }}>{children}</I18nContext.Provider>;
}

export function useT() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useT() must be used inside <I18nProvider>");
  return ctx;
}